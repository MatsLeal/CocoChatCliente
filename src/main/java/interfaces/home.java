    private void sendMessage(){
        
        if(this.Message_Chat.getText().isEmpty() ||  this.Jlbl_nombreconversacion.getText().isEmpty())
            return;
        
        
        Cliente.mandaMensaje(this.Message_Chat.getText(), this.Jlbl_nombreconversacion.getText());
        
        GuardadorConversaciones.GuardaMensaje(new Mensaje(this.Jlbl_minombre.getText(),
                                                    this.Message_Chat.getText(),
                                                    this.Jlbl_nombreconversacion.getText() 
                                                    ));
            Cliente.abreConversacion(this.Jlbl_nombreconversacion.getText(), Message_view);
            this.Message_Chat.setText("");
    }
    
    
    
