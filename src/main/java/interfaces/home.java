    private void Todos_list_conectadosMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Todos_list_conectadosMousePressed
        
        this.jbtn_agregaramigo.setVisible(true);
        
        if(this.Todos_list_conectados.getSelectedIndex()== -1)
            return;
        
        
        
        this.getJlbl_nombreconversacion().setText(this.Todos_list_conectados.getSelectedValue());
        Cliente.abreConversacion(this.Todos_list_conectados.getSelectedValue(), Message_view);
        
        try {
            if(Cliente.isFriend(this.Jlbl_nombreconversacion.getText()) 
                    //|| this.Jlbl_nombreconversacion.getText().equals(this.Jlbl_minombre.getText())
                    )
                this.jbtn_agregaramigo.setVisible(false);
        } catch (IOException ex) {
            Logger.getLogger(home.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_Todos_list_conectadosMousePressed

